package com.shanlin.demo.entity;

public class SysConfEntity {
    private String sysCode;
    private String sysConfCode;
    private String sysConfName;
    
    public String getSysCode() {
        return sysCode;
    }
    public void setSysCode(String sysCode) {
        this.sysCode = sysCode;
    }
    public String getSysConfCode() {
        return sysConfCode;
    }
    public void setSysConfCode(String sysConfCode) {
        this.sysConfCode = sysConfCode;
    }
    public String getSysConfName() {
        return sysConfName;
    }
    public void setSysConfName(String sysConfName) {
        this.sysConfName = sysConfName;
    }
}
